# 2. Classification Report'ları tablo haline getir ve yazdır

def report_to_df(report_str):
    report_data = []
    lines = report_str.strip().split('\n')
    for line in lines[2:-3]:
        row = line.strip().split()
        if len(row) < 4:  # Boşlukları düzenle
            continue
        report_data.append({
            "Class": row[0],
            "Precision": float(row[1]),
            "Recall": float(row[2]),
            "F1-Score": float(row[3]),
            "Support": int(row[4])
        })
    return pd.DataFrame(report_data)

# Raporları dataframeler haline getir
dfs = {}
for name, y_pred in y_preds.items():
    report = classification_report(y1_test, y_pred, target_names=class_names)
    dfs[name] = report_to_df(report)

# 3. Tabloyu görselleştir
fig, axes = plt.subplots(2, 2, figsize=(18, 5))
axes = axes.flatten()

for ax, (name, df_report) in zip(axes, dfs.items()):
    ax.axis('off')
    ax.set_title(f"{name} Classification Report")
    table = ax.table(cellText=df_report.round(3).values,
                     colLabels=df_report.columns,
                     cellLoc='center',
                     loc='center')
    table.auto_set_font_size(False)
    table.set_fontsize(12)
    table.auto_set_column_width(col=list(range(len(df_report.columns))))

plt.tight_layout()
